#how-it-works
	p Crowdsell is a crowdfunding system that offers a way to reward creators and thinkers for their contributions to society, without the outdated an ineffective system of legally controlled intellectual property.

	p A creator has done valuable creative work, like code, art, or designs, and wants to be rewarded for it.

	p The creator shows the world a preview of why the work is special, and makes a list of promises about the work's nature and quality.

	p The creator chooses a price they believe is fair, and the whole world can contribute to raise that amount.

	p When the amount is reached, the creator is paid the money, and the work is released under an open culture license free of legal restrictions. It belongs to everyone now.


